/**
 * 遍历数组？
 * 判断每一项后递归处理
 */
type Flatten<T extends any[], U = []> = T extends [infer First, ...infer Rest]
  ? First extends any[]
    ? [...Flatten<First>, ...Flatten<Rest>]
    : [First, ...Flatten<Rest>]
  : [];
